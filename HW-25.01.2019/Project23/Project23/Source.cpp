#include <iostream>
using namespace std;
int main() {
	int n;
	int * arr = new int[20];
	for (int i = 0; i < 20; i++) {
		arr[i] = 0;
	}
	cin >> n;
	int a, t;
	for (int i = 0; i < n; i++) {
		cin >> a;
		t = 0;
		while (a > 0) {
			t++;
			a /= 10;
		}
		arr[t]++;
	}
	int max = 0, l;
	for (int i = 1; i < 20; i++) {
		if (arr[i] >= max) {
			max = arr[i];
			l = i;
		}
	}
	cout << l << " " << max;
	system("pause");
	return 0;
}