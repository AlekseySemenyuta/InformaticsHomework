/*Напишите переборную программу, находящую для заданной последовательности
длины N неотрицательных целых чисел необходимо:минимальную сумму двух её элементов, 
номера которых различаются не менее чем на 5.
Программа может быть неэффективной*/
#include <iostream>
using namespace std;
int main() {
	int n;
	cin >> n;
	int *array = new int[n];
	for (int i = 0; i < n; i++) {
		cin >> array[i];
	}
	int min = array[0] + array[5];
	for (int i = 0; i < n; i++) {
		for (int j = i + 5; j < n; j++) {
			if ((array[i] + array[j]) < min) {
				min = array[i] + array[j];
			}
		}
	}
	cout << min;
	system("pause");
}